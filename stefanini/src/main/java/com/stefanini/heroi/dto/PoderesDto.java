package com.stefanini.heroi.dto;

public class PoderesDto {

	private String nomePoder;
	
	private String forca;
	
	private String classePoder;

	public String getNomePoder() {
		return nomePoder;
	}

	public void setNomePoder(String nomePoder) {
		this.nomePoder = nomePoder;
	}

	public String getForca() {
		return forca;
	}

	public void setForca(String forca) {
		this.forca = forca;
	}

	public String getClassePoder() {
		return classePoder;
	}

	public void setClassePoder(String classePoder) {
		this.classePoder = classePoder;
	}
	
	
	
}